x = 42
y = 12

s1 = "Stringa1"
s2 = "Stringa2"

print("Operazioni con numeri:")
print("x + y =", x + y)  # Addizione
print("x - y =", x - y)  # Sottrazione
print("x * y =", x * y)  # Moltiplicazione
print("x / y =", x / y)  # Divisione
print("x // y =", x // y)  # Divisione intera
print("x % y =", x % y)  # Modulo
print("x ** 3 =", x**3)  # Potenza

print("\nOperazioni con stringhe:")
print("s1 + s2 =", s1 + s2)  # Concatenazione
print("s1 * 3 =", s1 * 3)  # Ripetizione
print("'ing' in s1 =", "ing" in s1)  # Controllo sottostringa
print("len(s1) =", len(s1))  # Lunghezza
